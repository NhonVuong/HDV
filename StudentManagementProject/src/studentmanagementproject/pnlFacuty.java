    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        int row = jTable2.getSelectedRow();
        if(row<0) {
            JOptionPane.showMessageDialog(_Myparent, "Please, Choose a Faculty!");
            return;
        }
        MyTableModels<Faculty> Tablefc = (MyTableModels<Faculty>) jTable2.getModel();
        Faculty fc = Tablefc.GetRow(row);
        dlogADDFaculty frmadd = new dlogADDFaculty(_Myparent, true,fc);
        frmadd.setVisible(true);
        if(frmadd.getFaculty()!=null){
            int res =Integer.parseInt(_SupportDb.ResquestPost(urlUpdate,frmadd.getFaculty()));
            if(res<0) {
                JOptionPane.showMessageDialog(_Myparent, "Action Update Fail!");
            }else{
                MyTableModels<Faculty> mytable = (MyTableModels<Faculty>) jTable2.getModel();
                mytable.fireTableDataChanged();
            }
        }
        frmadd.dispose();
    }//GEN-LAST:event_btnUpdateActionPerformed

